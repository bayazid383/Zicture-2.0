if ($_SERVER['REQUEST_METHOD'] === 'POST' && isset($_POST['currency_select'])) {
    $selectedCurrency = strtoupper(trim($_POST['currency_select']));
    if (in_array($selectedCurrency, ['BDT', 'CNY', 'USD'], true)) {
        $_SESSION['currency'] = $selectedCurrency;
    }
}

function zicture_currency(): string {
    return $_SESSION['currency'] ?? 'BDT';
}

function zicture_money(float $amountBdt): string {
    $currency = zicture_currency();
    $rates = [
        'BDT' => ['symbol' => 'Tk', 'rate' => 1, 'decimals' => 0],
        'CNY' => ['symbol' => '¥', 'rate' => 0.061, 'decimals' => 2],
        'USD' => ['symbol' => '$', 'rate' => 0.0085, 'decimals' => 2],
    ];
    $meta = $rates[$currency] ?? $rates['BDT'];
    return $meta['symbol'] . ' ' . number_format($amountBdt * $meta['rate'], $meta['decimals']);
}

function zicture_plain_money(float $amountBdt): string {
    return zicture_money($amountBdt) . ' (' . zicture_currency() . ')';
}

